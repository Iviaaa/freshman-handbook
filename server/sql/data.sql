('银行开户', '银行开户


来到美国之后，拥有一个美国的银行账户是留学生的首先要做的事情之一。银行开户之后，即可方便的支付学费，同时也可以安放多余的资金以便今后使用。早点开启账户也是便于以后申请信用卡，为今后的生活和工作带来便利。
开户所需资料(不同的银行要求可能会有一些细节不一样，但是总体来以下两样是必备的): 
1） 证件两个。护照，美国驾照，i20 等都可以作为证件。
2）有效的合法签证
除此之外，还需要提供美国的居住地址和联系电话。


账户类型： 
学生需要用到的银行账户主要分成两种，一种叫支票账户(checking account)，一种叫储蓄账户(saving account). 
·存在支票账户里的钱可以随时取，空余的钱可以随时存进支票账户。在没有透支支票账户里的钱的情况下可以自由的使用。支票账户又分为有息账户和无息账户。有息账户一般要求支票账户李需要保留一定数量的最低金额，否则低于这个金额就会每个月收取少量的维护费。有息账户里的存款有比较低的年利率。无息账户，顾名思义不会随着储蓄时间的增长而增加，但是没有账户余额数目的要求。鉴于有息账户的利率较低，一般推荐办理无息的支票账户即可。
·不同于支票账户，存于储蓄账户的钱一般都是要存定期，对于存储的金额和时间根据利率不同都会有一定的要求。不同的银行在不同时期的利率有所不同。在短时间内需要用大笔金额的资金的时候，比如交学费，不要把钱放在储蓄账户上而是要放在支票账户中，以便于随时使用。当然在相当长的一段时间内没有太多的消费的话，可以将主要的资金放在储蓄账户中。
开户之后，你会拿到一张借记卡（debit card)，可以在各种场所使用支票账户里的钱进行消费，同时也可以在各家银行的自动提款机（ATM）提取现金。提取现金时如果不是在开户银行的ATM机办理，会收取一部分手续费。每一天可以提取的现金数量会有一定的限制，同时一次可以付款的金额也会有一定的限制，请注意向相关的银行咨询。

开户建议: 
在新生orientation 期间，各家银行会派专人来帮助新生办理开户手续， 大家可以多多咨询货比三家选择最适合自己的银行。建议大家选择在当地有比较多支行的并且在全国都有名的大银行，以Ann Arbor 为例Chase，PNC以及Bank of America 都是不错的选择。小银行一般会提供很高的储蓄利率来吸引新生，但是考虑到小银行的不稳定性，将大量资金储蓄在里面有一定的风险。同时小银行的ATM 机分布集中在很小的范围，一旦出门旅游就不得不缴纳不必要的手续费来提取现金了。相对而言，大银行在全国各地都会拥有数量可观的支行以及ATM机，方便为用户随时服务。除此之外，大银行的网上银行系统更加完善，许多和账户相关的操作都可以轻易的在网上完成。另一个好处是， 开启大银行的银行账户，有利于日后申请大银行的信用卡，比如chase 的freedom 以及蓝宝石卡都是回报非常高的信用卡。以上的信息是UMCSSA 为大家做的一个关于银行开户简单的总结与介绍，希望能够帮助大家顺利地迈出在美国生活得第一步。
