与中国相同，美国通讯也拥有几大知名运营商，如ATT，T-Mobile，Sprint和Verizon等等。各运营商提供服务大致相同，不同套餐价格所含流量，通话分钟数不同，信号强弱也略有分别。以下对主要的服务商进行对比：

AT&T : 中校信号好而北校表现不佳，在美国覆盖面较广，可签约的手机款式比较多，通常为多人共享套餐，单独办理资费较贵。

Verizon : 4G LTE 覆盖面广，可签约的手机款式比较多，价格较贵。

T-Mobile :  包含GSM与CDMA网络，资费相对便宜，但手机款式较少，T版定制手机可以使用中国运营商但不完全适配中国3G/4G网络。

Sprint : 手机款式不及 AT&T 或 Verizon 多，价格略高于 T-mobile，S版定制手机通常可以兼容中国运营商手机卡。

运营商分别出售Monthly Plan（月付卡），Prepaid（预付款）卡和Family Plan（家庭套餐）卡。Monthly Plan 这种是手机签约，一般需要签1～2年，每月支付固定费用，这个费用包含一定数量的电话分钟数、短信数量以及上网流量，签约会可获得免费的手机或者优惠购机。还有一种就是套餐签约，这样的套餐有不少价格要比签约机要更低一点，一般至少需要签约一年。而且刚到美国的时候，签手机时移动提供商需要缴纳金及提供护照等相关证件。如中途毁约会被处以罚金， 签约请谨慎。若选择合同制绑定购买套餐与手机，请向运营商了解清楚相关政策，避免可能的麻烦（如暑期回国需要 suspend）。

Prepaid手机卡每月定期扣款，提供一定数量的流量及通话分钟数，超额额外支付。类似国内的预付费卡。不需要支付月租费。用户可以先存任意价格的套餐，然后选择合适语音、短信和数据套餐进行消费。中途回国的话可以暂停不用，但请向运营商了解清楚相关政策，可能需要缴纳费。（中国电信美洲公司CTExcel提供免费保号三个月，可以回美国后继续使用，灵活度较高。）

Family Plan卡一般一个套餐可含4-5人，4-5人共享一定数额的流量及通话,其中一个人当保人，负责每月缴费，大家一起分享总数固定的分钟和流量。价格上Family Plan因为多人共享，会较prepaid卡低廉一些，但因为plan中可能存在部分人拖欠缴费，流量用超等问题，很难协调矛盾。现在随着更多优质实惠的Prepaid套餐的出现，Family Plan的价格优势不再明显。

